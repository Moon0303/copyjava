package cmm01.var;

class Var{

 public static void main(String []arg) {
  /*
   * 논리타입 : boolean 
   * 정수형 : int
   * 실수형 : float , double
   * */
  int x;
  
  x= 1;
  
  int y;
  
  y= x+1;
  
  
  
  System.out.println();
  /*
   * "" 문자 출력 , C언어와다르게 바로 변수 적용가능 */
  System.out.println("x+1의 값"+y);
 }
 
}
